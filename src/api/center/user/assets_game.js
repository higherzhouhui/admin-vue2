import request from '@/utils/request'

/**
 * 游戏进出明细
 * uid
 * type 0 游戏金币代入 1游戏金币代出
 * uid 收礼者ID
 * pickerValue [开始时间,结束时间]
 */
export function getUserAssetsGameList(pageSize = 20, pageNum = 1, data) {
  var url = `/center/user/game?pageSize=${pageSize}&pageNum=${pageNum}`
  if (data.uid != null && data.uid != undefined && `${data.uid}` != 'null' && `${data.uid}` != '') {
    url += `&uid=${data.uid}`
  }
  if (data.type != null && data.type != undefined && `${data.type}` != 'null') {
    url += `&type=${data.type}`
  }
  // 处理时间
  var startTime = null;
  var endTime = null;
  if (data.pickerValue != null && JSON.stringify(data.pickerValue) != '[]' && Array.isArray(data.pickerValue)) {
    console.error(JSON.stringify(data.pickerValue));
    let values = data.pickerValue;
    startTime = values[0];
    endTime = values[1];
  }
  if (startTime != null && startTime != undefined && `${startTime}` != 'null') {
    url += `&startTime=${startTime}`
  }
  if (endTime != null && endTime != undefined && `${endTime}` != 'null') {
    url += `&endTime=${endTime}`
  }
  return request({
    url: url,
    method: 'get'
  })
}

